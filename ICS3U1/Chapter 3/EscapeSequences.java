// A backslash (\) is a ESCAPE SEQUENCE.
public class EscapeSequences {
  public static void main(String[] args) {
    
    System.out.println("\"You miss 100% of the shots you don't take\"");
    System.out.println("A wild backslash has been caught \\");
    System.out.println("Tab\tGang");
    System.out.println("\u00D8 Unicode Gang");
    System.out.println("Every\nnword\nis\ngreat");
  }
}