import java.util.Scanner;
public class floatSum {
  public static void main(String[] args) {
    Scanner sc = new Scanner(System.in);
    double input1 = sc.nextDouble();
    double input2 = sc.nextDouble();
    System.out.println(input1 + input2);
    System.out.println(input1 - input2);
    System.out.println(input1 * input2);
  }
}